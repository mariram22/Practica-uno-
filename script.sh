1. Copiar url del repositorio que se va a copiar 
2. Usar git clone URL  para descargar el repositorio
3. crear un archivo 
4. add . para agregar el cambio 
5. git commit para agregar mensajes de lo que se realizo para que se agregue a la rama 
6. Git pull para actualizar la carpeta 
7. luego en git hub se pone push para mandar los cambios al repositorio 

